def listAdd(array,addTo):
	array = list(array)
	array.append(addTo)
	print(array)

def listRemove(array,delete):
	array = list(array)
	array.remove(delete)
	print(array)

def listInsert(array,insert):
	array = list(array)
	array.insert(insert)
	print(array)

def listPop(array,index):
	array = list(array)
	array.pop(index)
	print(array)

def listLoop(array):
	for x in array:
		print(x)

